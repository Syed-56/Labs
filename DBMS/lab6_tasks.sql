SELECT FIRST_NAME, LAST_NAME, EMPLOYEE_ID, MANAGER_ID FROM EMPLOYEES E
WHERE EXISTS (SELECT 1 FROM EMPLOYEES WHERE E.EMPLOYEE_ID=MANAGER_ID);

--task16
SELECT FIRST_NAME, LAST_NAME, EMPLOYEE_ID FROM EMPLOYEES E
WHERE NOT EXISTS (SELECT 1 FROM EMPLOYEES WHERE E.EMPLOYEE_ID=MANAGER_ID);

--task17
SELECT DEPARTMENT_NAME, DEPARTMENT_ID FROM DEPARTMENTS D
WHERE NOT EXISTS (SELECT 1 FROM EMPLOYEES E WHERE D.DEPARTMENT_ID=E.DEPARTMENT_ID);

--task18
SELECT DEPARTMENT_NAME, DEPARTMENT_ID FROM DEPARTMENTS D
WHERE EXISTS (SELECT 1 FROM EMPLOYEES E WHERE E.DEPARTMENT_ID=D.DEPARTMENT_ID AND E.SALARY>10000);

--task19
SELECT DEPARTMENT_ID, (SELECT DEPARTMENT_NAME FROM DEPARTMENTS D WHERE D.DEPARTMENT_ID=DEPT_STATS.DEPARTMENT_ID) AS DEPT_NAME, AVG_SALARY
FROM (SELECT DEPARTMENT_ID, ROUND(AVG(SALARY),1) AS AVG_SALARY FROM EMPLOYEES GROUP BY DEPARTMENT_ID) DEPT_STATS
WHERE AVG_SALARY>8000;

--task20
SELECT DEPARTMENT_ID, (SELECT DEPARTMENT_NAME FROM DEPARTMENTS D WHERE D.DEPARTMENT_ID=DEPT_STATS.DEPARTMENT_ID) AS DEPT_NAME, AVG_SALARY
FROM (SELECT ROUND(AVG(SALARY),1) AS AVG_SALARY, DEPARTMENT_ID FROM EMPLOYEES GROUP BY DEPARTMENT_ID) DEPT_STATS
ORDER BY AVG_SALARY DESC
FETCH FIRST 3 ROWS ONLY;