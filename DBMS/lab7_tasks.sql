SELECT CONCAT(E.FIRST_NAME,' ',E.LAST_NAME) AS EMP_NAME, D.DEPARTMENT_NAME, L.CITY FROM EMPLOYEES E
JOIN DEPARTMENTS D ON E.DEPARTMENT_ID=D.DEPARTMENT_ID
JOIN LOCATIONS L ON D.LOCATION_ID=L.LOCATION_ID;

--task12
CREATE VIEW EMP_DEPT_VIEW AS 
SELECT E.EMPLOYEE_ID, E.FIRST_NAME, E.LAST_NAME, D.DEPARTMENT_NAME FROM EMPLOYEES E
JOIN DEPARTMENTS D ON E.DEPARTMENT_ID=D.DEPARTMENT_ID;
SELECT * FROM EMP_DEPT_VIEW;

--task13
CREATE VIEW RICH_EMPS AS
SELECT * FROM (SELECT CONCAT(FIRST_NAME,' ',LAST_NAME) AS EMP_NAME, SALARY FROM EMPLOYEES)
WHERE SALARY > (SELECT AVG(SALARY) FROM EMPLOYEES);
SELECT * FROM RICH_EMPS;

