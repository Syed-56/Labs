SELECT E.FIRST_NAME, E.LAST_NAME, D.DEPARTMENT_NAME FROM EMPLOYEES E
JOIN DEPARTMENTS D ON E.DEPARTMENT_ID=D.DEPARTMENT_ID;

--task2
SELECT E.FIRST_NAME, E.LAST_NAME, J.JOB_TITLE FROM EMPLOYEES E
JOIN JOBS J ON E.JOB_ID=J.JOB_ID;

--task3
SELECT FIRST_NAME, LAST_NAME, LOCATION_ID FROM EMPLOYEES 
JOIN DEPARTMENTS USING (DEPARTMENT_ID) JOIN LOCATIONS USING (LOCATION_ID);

--task4
SELECT FIRST_NAME,LAST_NAME,DEPARTMENT_NAME FROM EMPLOYEES NATURAL JOIN DEPARTMENTS;

--task5
SELECT CONCAT(E.FIRST_NAME,' ',E.LAST_NAME) AS EMP_NAME, CONCAT(M.FIRST_NAME,' ',M.LAST_NAME) AS MANAGER_NAME FROM EMPLOYEES E 
JOIN EMPLOYEES M ON E.MANAGER_ID=M.EMPLOYEE_ID;

--task6
SELECT CONCAT(E.FIRST_NAME,' ',E.LAST_NAME) AS EMP_NAME, D.DEPARTMENT_NAME FROM EMPLOYEES E 
LEFT JOIN DEPARTMENTS D ON E.DEPARTMENT_ID=D.DEPARTMENT_ID;

